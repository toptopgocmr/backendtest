<h1>Politique de Confidentialité</h1>
<p class="last-update">Dernière mise à jour : Mai 2026</p>

<p>
    La présente politique de confidentialité décrit comment <strong>TholadImmo</strong> (Tholad Group),
    accessible via l'application mobile <strong>TholadImmo</strong> sur Google Play,
    collecte, utilise et protège vos données personnelles.
</p>

<h2>1. Données collectées</h2>
<p>Lors de l'utilisation de notre application, nous pouvons collecter les informations suivantes :</p>
<ul>
    <li>Nom complet, adresse e-mail, numéro de téléphone</li>
    <li>Photos de profil</li>
    <li>Informations relatives aux biens immobiliers publiés (photos, adresse, description)</li>
    <li>Données de réservation et de paiement (référence de transaction Mobile Money, captures d'écran de paiement)</li>
    <li>Messages échangés entre utilisateurs et propriétaires</li>
    <li>Données de localisation (ville de recherche, uniquement si vous y consentez)</li>
</ul>

<h2>2. Utilisation des données</h2>
<p>Vos données sont utilisées pour :</p>
<ul>
    <li>Gérer votre compte et authentifier votre identité</li>
    <li>Traiter et suivre vos réservations de biens immobiliers</li>
    <li>Valider les paiements effectués via Mobile Money (MTN, Airtel)</li>
    <li>Vous envoyer des notifications relatives à votre réservation ou votre compte</li>
    <li>Améliorer nos services et l'expérience utilisateur</li>
    <li>Respecter nos obligations légales</li>
</ul>

<h2>3. Partage des données</h2>
<p>Nous ne vendons pas vos données personnelles. Elles peuvent être partagées avec :</p>
<ul>
    <li><strong>Cloudinary</strong> — hébergement des images (photos de biens et preuves de paiement)</li>
    <li><strong>Railway / Services d'hébergement</strong> — infrastructure technique de l'application</li>
    <li>Les propriétaires de biens, dans le cadre strict de votre réservation</li>
    <li>Les autorités légales, si la loi l'exige</li>
</ul>

<h2>4. Conservation des données</h2>
<p>
    Vos données sont conservées aussi longtemps que votre compte est actif ou que nécessaire
    pour fournir nos services. Vous pouvez demander la suppression de votre compte et de vos
    données à tout moment en nous contactant.
</p>

<h2>5. Sécurité</h2>
<p>
    Nous mettons en œuvre des mesures techniques et organisationnelles appropriées pour protéger
    vos données contre tout accès non autorisé, perte ou divulgation.
</p>

<h2>6. Droits des utilisateurs</h2>
<p>Conformément à la réglementation applicable, vous avez le droit de :</p>
<ul>
    <li>Accéder à vos données personnelles</li>
    <li>Corriger des informations inexactes</li>
    <li>Demander la suppression de vos données</li>
    <li>Retirer votre consentement à tout moment</li>
</ul>

<h2>7. Contact</h2>
<p>
    Pour toute question relative à cette politique ou pour exercer vos droits, contactez-nous à :<br>
    📧 <a href="mailto:contact@tholadimmo.com">contact@tholadimmo.com</a><br>
    📍 Pointe-Noire, République du Congo
</p>

<h2>8. Modifications</h2>
<p>
    Nous nous réservons le droit de modifier cette politique à tout moment.
    Les utilisateurs seront informés de tout changement significatif via l'application.
</p>

